package chapter3.src;

public class Human {
	
	private String name;
	private int age;
	private String phoneNum;
	
	
	public Human(String name) {
		this.name = name;
	}
	
	public String getName(){
		return this.name;
	}

	public int getAge() {
		return age;
	}

	public String getPhoneNum() {
		return phoneNum;
	}
	
	

}
